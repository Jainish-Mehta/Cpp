#include <iostream>
#include <cmath>
using namespace std;

int main() {
    cout << "sqrt(16) = " << sqrt(16) << endl;
    cout << "pow(2, 3) = " << pow(2, 3) << endl;
    cout << "abs(-10) = " << abs(-10) << endl;
    cout << "ceil(4.2) = " << ceil(4.2) << endl;
    cout << "floor(4.8) = " << floor(4.8) << endl;
    return 0;
}
